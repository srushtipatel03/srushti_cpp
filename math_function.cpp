#include<iostream>
#include<math.h>

using namespace std;

int main()
{
    cout <<"Ceil Number : " << ceil(7.5) << endl;
    cout <<"Ceil Number : " << ceil(7.2) << endl;
    cout <<"Ceil Number : " << ceil(7.9) << endl;
    cout <<"Ceil Number : " << ceil(7.6) << endl;

    cout <<"Floor Number : " << floor(7.2) << endl;
    cout <<"Floor Number : " << floor(7.5) << endl;
    cout <<"Floor Number : " << floor(7.9) << endl;
    cout <<"Floor Number : " << floor(7.6) << endl;

    cout <<"Round Number : " << round(7.6) << endl;
    cout <<"Round Number : " << round(8.4) << endl;
    cout <<"Round Number : " << round(7.4) << endl;
    cout <<"Round Number : " << round(8.6) << endl;

    cout <<"sqrt Number : " << sqrt(25) << endl;
    cout <<"sqrt Number : " << sqrt(8) << endl;
    cout <<"sqrt Number : " << sqrt(12) << endl;
    cout <<"sqrt Number : " << sqrt(512) << endl;

    cout <<"Power Number : " << pow(7,6) << endl;
    cout <<"Power Number : " << pow(6,2) << endl;
    cout <<"Power Number : " << pow(4,5) << endl;
    cout <<"Power Number : " << pow(9,3) << endl;

    cout <<"abs Number : " << abs(-76) << endl;
    cout <<"abs Number : " << abs(-96) << endl;
    cout <<"abs Number : " << abs(77) << endl;
    cout <<"abs Number : " << abs(-65) << endl;
}